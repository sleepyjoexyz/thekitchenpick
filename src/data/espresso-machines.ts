export function getEspressoMachinesByPriceRange(
  range: "budget" | "mid" | "premium" | "luxury"
): Product[] {
  return espressoMachines.filter((m) => m.priceRange === range);
}

export function getEspressoMachinesByType(
  type: "semi-automatic" | "super-automatic" | "pod/capsule" | "manual/lever"
): Product[] {
  return espressoMachines.filter((m) => m.type === type);
}

export function getEspressoMachinesWithGrinder(): Product[] {
  return espressoMachines.filter((m) => m.hasGrinder);
}

export function sortMachinesByPrice(
  machines: Product[],
  order: "asc" | "desc" = "asc"
): Product[] {
  return [...machines].sort((a, b) =>
    order === "asc" ? a.price - b.price : b.price - a.price
  );
}

export function sortMachinesByRating(
  machines: Product[],
  order: "asc" | "desc" = "desc"
): Product[] {
  return [...machines].sort((a, b) =>
    order === "asc" ? a.rating - b.rating : b.rating - a.rating
  );
}
