export function getRobotVacuumsByPriceRange(
  range: "budget" | "mid" | "premium" | "luxury"
): RobotVacuum[] {
  return robotVacuums.filter((v) => v.priceRange === range);
}

export function getRobotVacuumsWithSelfEmpty(): RobotVacuum[] {
  return robotVacuums.filter((v) => v.selfEmpty);
}

export function getRobotVacuumsWithMopping(): RobotVacuum[] {
  return robotVacuums.filter((v) => v.mopping);
}

export function getRobotVacuumsByNavigation(
  navigation: string
): RobotVacuum[] {
  return robotVacuums.filter((v) => v.navigation === navigation);
}

export function sortVacuumsByPrice(
  vacuums: RobotVacuum[],
  order: "asc" | "desc" = "asc"
): RobotVacuum[] {
  return [...vacuums].sort((a, b) =>
    order === "asc" ? a.price - b.price : b.price - a.price
  );
}

export function sortVacuumsByRating(
  vacuums: RobotVacuum[],
  order: "asc" | "desc" = "desc"
): RobotVacuum[] {
  return [...vacuums].sort((a, b) =>
    order === "asc" ? a.rating - b.rating : b.rating - a.rating
  );
}

export function sortVacuumsBySuction(
  vacuums: RobotVacuum[],
  order: "asc" | "desc" = "desc"
): RobotVacuum[] {
  return [...vacuums].sort((a, b) =>
    order === "asc" ? a.suctionPa - b.suctionPa : b.suctionPa - a.suctionPa
  );
}
