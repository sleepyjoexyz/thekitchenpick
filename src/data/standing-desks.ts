export function getStandingDesksByPriceRange(
  range: "budget" | "mid" | "premium" | "luxury"
): StandingDesk[] {
  return standingDesks.filter((d) => d.priceRange === range);
}

export function getStandingDesksByMotorType(motorType: string): StandingDesk[] {
  return standingDesks.filter((d) => d.motorType === motorType);
}

export function getStandingDesksWithKeyboardTray(): StandingDesk[] {
  return standingDesks.filter((d) => d.hasKeyboardTray);
}

export function getStandingDesksWithMemoryPresets(
  minPresets: number
): StandingDesk[] {
  return standingDesks.filter((d) => d.memoryPresets >= minPresets);
}

export function sortDesksByPrice(
  desks: StandingDesk[],
  order: "asc" | "desc" = "asc"
): StandingDesk[] {
  return [...desks].sort((a, b) =>
    order === "asc" ? a.price - b.price : b.price - a.price
  );
}

export function sortDesksByRating(
  desks: StandingDesk[],
  order: "asc" | "desc" = "desc"
): StandingDesk[] {
  return [...desks].sort((a, b) =>
    order === "asc" ? a.rating - b.rating : b.rating - a.rating
  );
}
